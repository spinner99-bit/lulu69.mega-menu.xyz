// 购物车初始化
let cart = JSON.parse(localStorage.getItem('cart')) || [];

// 页面加载时显示产品
window.onload = function() {
    loadCategories();
    loadProducts("all");
    updateCartCount();
};

// 加载分类
function loadCategories() {
    const categories = [...new Set(products.map(product => product.category))];
    const categorySelect = document.getElementById("category");
    categories.forEach(category => {
        const option = document.createElement("option");
        option.value = category;
        option.textContent = category;
        categorySelect.appendChild(option);
    });
    categorySelect.addEventListener("change", function() {
        loadProducts(this.value);
    });
}

// 加载产品
function loadProducts(category) {
    const productList = document.getElementById("productList");
    productList.innerHTML = "";
    const filteredProducts = category === "all" ? products : products.filter(product => product.category === category);
    
    filteredProducts.forEach(product => {
        const productItem = document.createElement("div");
        productItem.classList.add("product-item");

        const isInCart = cart.some(p => p.id === product.id);

        productItem.innerHTML = `
            <img src="${product.img}" alt="${product.name}">
            <div class="product-name">${product.name}</div>
            <div class="product-price">RM ${product.price}</div>
            <button class="${isInCart ? 'added-to-cart-btn' : 'add-to-cart-btn'}" onclick="addToCart(${product.id})">
                ${isInCart ? '已添加' : '加入购物车'}
            </button>
        `;
        productList.appendChild(productItem);
    });
}

// 添加产品到购物车
function addToCart(productId) {
    const product = products.find(p => p.id === productId);
    if (!cart.some(p => p.id === productId)) {
        cart.push(product);
        updateCartCount();
        localStorage.setItem('cart', JSON.stringify(cart));
        alert(`${product.name} 已加入购物车！`);
        loadProducts(document.getElementById("category").value);
    }
}

// 更新购物车数量
function updateCartCount() {
    document.getElementById("cartCount").textContent = cart.length;
}

// 显示购物车列表
document.getElementById("cartIcon").addEventListener("click", function() {
    document.getElementById("cartList").classList.toggle("visible");
    loadCartItems();
});

// 加载购物车产品
function loadCartItems() {
    const cartItemsDiv = document.getElementById("cartItems");
    cartItemsDiv.innerHTML = "";
    cart.forEach(item => {
        const cartItem = document.createElement("div");
        cartItem.classList.add("cart-item");
        cartItem.innerHTML = `
            <img src="${item.img}" alt="${item.name}">
            <span>${item.name} - RM ${item.price}</span>
            <button onclick="removeFromCart(${item.id})">删除</button>
        `;
        cartItemsDiv.appendChild(cartItem);
    });
}

// 从购物车移除产品
function removeFromCart(productId) {
    cart = cart.filter(p => p.id !== productId);
    localStorage.setItem('cart', JSON.stringify(cart));
    loadCartItems();
    updateCartCount();
}

// 清空购物车
document.getElementById("clearCart").addEventListener("click", function() {
    cart = [];
    localStorage.setItem('cart', JSON.stringify(cart));
    loadCartItems();
    updateCartCount();
});

// 关闭购物车列表
document.getElementById("closeCart").addEventListener("click", function() {
    document.getElementById("cartList").classList.remove("visible");
});

// 发送购物车内容到电报
document.getElementById("sendCart").addEventListener("click", function() {
    if (cart.length === 0) {
        alert("购物车为空，无法发送！");
        return;
    }

    const cartDetails = cart.map(item => `${item.name} - RM ${item.price}`).join("\n");
    const transactionId = Math.floor(Math.random() * 1000000);
    const message = `NEW ORDER INCOMING\n\n${cartDetails}\n\nTransaction ID : ${transactionId}`;
    
    // 发送购物车内容至电报
    sendToTelegram(message);
    
    alert(`购物车内容已发送！交易编号: ${transactionId}。请复制该编号并联系客服完成订单。`);
    
    // 清空购物车
    cart = [];
    localStorage.setItem('cart', JSON.stringify(cart));
    loadCartItems();
    updateCartCount();
});

// 发送电报消息的函数
function sendToTelegram(message) {
    const telegramBotId = "6414565524:AAGY2obKsjvpfyH8rnq4t9OWMPDgKHM8ddI";
    const chatId = "-4552302294";
    const url = `https://api.telegram.org/bot${telegramBotId}/sendMessage?chat_id=${chatId}&text=${encodeURIComponent(message)}`;

    fetch(url)
        .then(response => response.json())
        .then(data => {
            if (data.ok) {
                console.log("消息已发送至电报");
            } else {
                console.error("电报消息发送失败:", data);
            }
        })
        .catch(error => {
            console.error("请求失败:", error);
        });
}
